package vo;

public class AccountVo {

	private String acc_no;
	private String member_ID;
	private int type_idx;
	private String acc_s;
	private String acc_e;
	
	
	public String getAcc_no() {
		return acc_no;
	}
	public void setAcc_no(String acc_no) {
		this.acc_no = acc_no;
	}
	public String getMember_ID() {
		return member_ID;
	}
	public void setMember_ID(String member_ID) {
		this.member_ID = member_ID;
	}
	public int getType_idx() {
		return type_idx;
	}
	public void setType_idx(int type_idx) {
		this.type_idx = type_idx;
	}
	public String getAcc_s() {
		return acc_s;
	}
	public void setAcc_s(String acc_s) {
		this.acc_s = acc_s;
	}
	public String getAcc_e() {
		return acc_e;
	}
	public void setAcc_e(String acc_e) {
		this.acc_e = acc_e;
	}
	
	
}
